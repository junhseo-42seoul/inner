#include "include/ft_printf.h"

void	put_u(unsigned int n, int fd)
{
	char	tmp;

	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	tmp = (n % 10) + '0';
	write(fd, &tmp, 1);
}
